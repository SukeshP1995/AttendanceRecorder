package com.example.studenttrackerapp;

public class Fields {
    public String date;
    public String leaves;
    public String session1;
    public String session2;
    public String session3;

    Fields(String date, String leaves, String session1, String session2, String session3) {
        this.date = date;
        this.leaves = leaves;
        this.session1 = session1;
        this.session2 = session2;
        this.session3 = session3;
    }
}
